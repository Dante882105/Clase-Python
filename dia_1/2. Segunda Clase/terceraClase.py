#Operadores matemáticos
x = 6
y = 2
z = 7

print(f"{x} más {y} es igual a {x + y}")
print(f"{x} menos {y} es igual a {x - y}")
print(f"{x} por {y} es igual a {x * y}")
print(f"{x} dividido {y} es igual a {x / y}")
#división al piso
print(f"{z} dividido y aproximado al valor menor de {y} es igual a {z // y}")
#Modulo (usado normalmente en identificación de números pares)
print(f"{z} módulo de {y} es igual a {z % y}")
#Potenciación
print(f"{z} elevado a la {y} es igual a {z ** y}")
#Raíz cuadrada
print(f"la raíz cuadrada de es {z}  igual a {z ** 0.5}")
